#include<stdio.h>

int main()
{
    int a,b,c,d;
    a=40;
    b=6;
    c=2;
    d=0;
    printf("a + b = %d\n",a+b);
    printf("a - b = %d\n",a-b);
    printf("a * b = %d\n",a*b);
    printf("a / b = %d\n",a/b);
    printf("c == d = %d\n",c==d);
    printf("c && d = %d\n",c&&d);
    printf("c || d = %d\n",c||d);
    printf("! c = %d\n",!c);
    printf("! d = %d\n",!d);
    return 0;
}
